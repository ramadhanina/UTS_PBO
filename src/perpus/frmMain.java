    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int baris = jTable1.getSelectedRow();
        if (baris != 1|| baris != 0) {
            txtNA.setText(jTable1.getValueAt(baris, 0).toString());
            txtNama.setText(jTable1.getValueAt(baris, 1).toString());
            txtAlamat.setText(jTable1.getValueAt(baris, 2).toString());
            if ("Buku Pelajaran".equals(jTable1.getValueAt(baris, 3).toString())) {
                btnpelajaran.setSelected(true);
            } else if ("Buku Bacaan".equals(jTable1.getValueAt(baris, 3).toString())){
                btnbacaan.setSelected(true);
            } else {
                btnnovel.setSelected(true);
            }
            txtjudul.setText(jTable1.getValueAt(baris, 4).toString());
            SimpleDateFormat date = new SimpleDateFormat("yyy-MM-dd");
            Date tgl1 = null;
            Date tgl2 = null;
            
            try {
                tgl1 = date.parse(jTable1.getValueAt(baris, 5).toString());
                tgl2 = date.parse(jTable1.getValueAt(baris, 6).toString());
            } catch (ParseException ex){
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            jcalpinjam.setDate(tgl1);
            jcalkembali.setDate(tgl2);
            txtstatus.setText(jTable1.getValueAt(baris, 7).toString());
            
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked

