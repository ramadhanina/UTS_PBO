     public void selectData(){
        String kolom[] = {"No_Anggota","Nama_Peminjam","Alamat_Peminjam","Jenis_Buku","Judul_Buku","Tanggal_Pinjam","Tanggal_Kembali","Status"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_kembali";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()){
                
                
                String No_Anggota = rs.getString(1);
                String Nama_Peminjam = rs.getString(2);
                String Alamat_Peminjam = rs.getString(3);
                String Jenis_Buku = "";
                if ("Buku Pelajaran".equals(rs.getString(4))) {
                    Jenis_Buku = "Buku Pelajaran";
                } else if ("Buku Bacaan".equals(rs.getString(4))){
                    Jenis_Buku = "Buku Bacaan";
                } else {
                    Jenis_Buku = "Buku Novel";
                }
                String Judul_Buku = rs.getString(5);
                String Tanggal_Pinjam = rs.getString(6);
                String Tanggal_Kembali = rs.getString(7);
                String Status = rs.getString(8);
              
                String data[] = {No_Anggota,Nama_Peminjam,Alamat_Peminjam,Jenis_Buku,Judul_Buku,Tanggal_Pinjam,Tanggal_Kembali,Status};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTable1.setModel(dtm);
    }
     
